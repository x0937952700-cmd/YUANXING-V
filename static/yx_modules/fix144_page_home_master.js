(function(){'use strict';
  const M=()=>window.YX144; if(!M())return;
  M().register('page_home_master',{install({moduleKey}){ if(moduleKey()!=='home')return;
    document.documentElement.dataset.yx133HomeBg='locked';
    const labels=[['/inventory','庫存'],['/orders','訂單'],['/master-order','總單'],['/ship','出貨'],['/shipping-query','出貨查詢'],['/warehouse','倉庫圖'],['/customers','客戶資料'],['/todos','代辦事項'],['/settings','設定'],['/today-changes','今日異動']];
    document.querySelectorAll('a').forEach(a=>{const href=a.getAttribute('href')||''; if(a.textContent.trim())return; const hit=labels.find(x=>href.includes(x[0])); if(hit)a.textContent=hit[1];});
    document.querySelectorAll('.home-menu .menu-btn').forEach(btn=>{btn.classList.add('yx144-home-main-btn');});
    document.querySelectorAll('.home-top-actions .home-mini-btn').forEach(btn=>{btn.classList.add('yx144-home-mini-btn');});
  }});
  M().install('page_home_master',true);
})();
